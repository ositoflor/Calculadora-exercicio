package calculadora.app;

public class Numeros {
    double numero1;
    double numero2;

    double resultAdicao() {
      return  numero1 + numero2;
    };

    double resultSubtracao() {
        return  numero1 - numero2;
    };

    double resultMultiplicacao() {
        return  numero1 * numero2;
    };

    double resultDivisao() {
        return  numero1 / numero2;
    };
}
